console.log(Math.abs(-5));//(Lấy giá trị dương của -5)
console.log(Math.abs(5)); //(Số dương không đổi)
console.log(Math.abs(10));//(Số dương không đổi)

console.log(Math.ceil(4.4));//(Làm tròn lên từ 4.4 thành 5)
console.log(Math.ceil(5.8));//(Làm tròn lên từ 5.8 thành 6)

console.log(Math.floor(5.7));//(Làm tròn xuống từ 5.7 thành 5)
console.log(Math.floor(6.1));//(Làm tròn xuống từ 6.1 thành 6)

console.log(Math.round(10.1));//(10.1 gần 10 hơn 11 nên làm tròn về 10)
console.log(Math.round(20.6));//(20.6 gần 21 hơn 20 nên làm tròn lên 21)
